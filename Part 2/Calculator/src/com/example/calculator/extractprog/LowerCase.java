package com.example.calculator.extractprog;

public class LowerCase {

    public static boolean isUpperCase(char c) {
        return (c >= 'A' && c <= 'Z');
    }

    public static boolean isUpperCaseVowel(char c) {
        return c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U';
    }
}



